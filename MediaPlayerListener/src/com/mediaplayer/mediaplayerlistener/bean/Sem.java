package com.mediaplayer.mediaplayerlistener.bean;

public class Sem {

	private String domain ;
	
	private String Fun ;
	
	private String FunA ;
	
	private String FunB ;
	
	private String FunC ;
	
	public String getFunC() {
		return FunC;
	}

	public void setFunC(String funC) {
		FunC = funC;
	}

	public String getFunB() {
		return FunB;
	}

	public void setFunB(String funB) {
		FunB = funB;
	}

	public String getFunA() {
		return FunA;
	}

	public void setFunA(String funA) {
		FunA = funA;
	}

	private String action ;

	public String getDomain() {
		return domain;
	}

	public void setDomain(String domain) {
		this.domain = domain;
	}

	public String getFun() {
		return Fun;
	}

	public void setFun(String fun) {
		Fun = fun;
	}

	public String getAction() {
		return action;
	}

	public void setAction(String action) {
		this.action = action;
	}
	
}
